package loops;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		/*
		 * Syntax 
		 * 
		 * do {
		 * 
		 * // code to be executed
		 * 
		 * } while (condition);
		 * 
		 */
		
		
		int i = 0;
        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 10);
		
	}

}
